define(function(){
    /*账单收缩*/
    $('.bill-menu').click(function(){
        var userasset = $(this).siblings('.bill-ul2');
        if(userasset.is(':hidden')){
            userasset.stop(true,true).slideDown(300);
            $(this).addClass('click');
        }else{
            userasset.stop(true,true).slideUp(300);
            $(this).removeClass('click');
        }
    });
    $('.bill-title').click(function(){
        var userasset = $(this).siblings('.bill-ul');
        if(userasset.is(':hidden')){
            userasset.stop(true,true).slideDown(300);
            $(this).addClass('click');
        }else{
            userasset.stop(true,true).slideUp(300);
            $(this).removeClass('click');
        }
    });
})
