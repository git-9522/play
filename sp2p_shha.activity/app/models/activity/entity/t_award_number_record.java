package models.activity.entity;

import java.util.Date;

import javax.persistence.Entity;

import play.db.jpa.Model;

/**
 * 获奖号码表
 * 
 * @author shianliang
 * 
 */
@Entity
public class t_award_number_record extends Model {

	/** 用户名 */
	public String user_name;

	/** userId */
	public long user_id;
	/** 投资金额 */
	public double amount;
	/** 本次发放抽奖号码个数 **/
	public int count;
	/** 时间 */
	public Date time;
	/** 摇奖期数 */
	public int periods;
	/** 起止号码 */
	public String min_award_number;
	/** 最大号码 */
	public String max_award_number;

}
